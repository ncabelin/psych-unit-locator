// insert firebase

function addLocation(name, address, tel, info, ins, lat, long) {
  var data = {
    name: name,
    address: address,
    tel: tel,
    info: info,
    ins: ins,
    lat: lat,
    long: long
  };

  // create new post key
  var newPostKey = firebase.database().ref().child('locations').push().key;

  var updates = {};
  updates['/locations/' + newPostKey] = data;

  // updates firebase
  return firebase.database().ref().update(updates);
}

